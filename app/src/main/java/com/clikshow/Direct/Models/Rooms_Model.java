package com.clikshow.Direct.Models;

public class Rooms_Model {
    String id;
    String last_message;
    String name;
    String thumb;
    String username;
    String timestamp;

    public Rooms_Model(){}

    public String getId() {
        return id;
    }

    public String getLast_message() {
        return last_message;
    }

    public String getName() {
        return name;
    }

    public String getThumb() {
        return thumb;
    }

    public String getUsername() {
        return username;
    }

    public String getTimestamp() {
        return timestamp;
    }
}
